        private static byte Lookup(char c) => LookupTable[c];
        public static byte ToByte(string chars, int offset) => (byte)(Lookup(chars[offset]) << 4 | Lookup(chars[offset + 1]));

        public static byte[] ToBytes(string inputString)
        {
            var byteList = new List<byte>(inputString.Length / 2);
            for (var i = 0; i < inputString.Length;)
            {
                if (inputString[i] == ' ' || inputString[i] == '\n' || inputString[i] == '\r' || inputString[i] == '\t' || inputString[i] == '-')
                {
                    i++;
                    continue;
                }
                byteList.Add(ToByte(inputString, i));
                i += 2;
            }
            return byteList.ToArray();
        }
