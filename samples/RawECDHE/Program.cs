using System;
using System.Diagnostics;

namespace RawECDHE
{
    class Program
    {
        static readonly byte[] _clientKey = { 0x04, 0x47, 0xD4, 0x9E, 0x92, 0x82, 0x09, 0x85, 0xB4, 0x5A, 0x10, 0x0C, 0xE1, 0xA2, 0x77, 0x27, 0xEB, 0x18, 0xF4, 0x3B, 0x01, 0xE4, 0x3F, 0x04, 0x25, 0xB0, 0xC8, 0x94, 0x71, 0x1E, 0x3C, 0x55, 0xEF, 0x9C, 0xCF, 0xE4, 0xC4, 0xC2, 0x3C, 0xBC, 0x14, 0x07, 0x32, 0xBF, 0x3D, 0xDF, 0x9D, 0x9F, 0xD3, 0xAD, 0x9F, 0x92, 0x22, 0xA2, 0x77, 0x2E, 0xF3, 0xB0, 0xF8, 0xD9, 0x44, 0x76, 0xA0, 0xED, 0x21 };

        static void Main(string[] args)
        {
            var numberOfIterations = 100000;

            var open = new OpenSslTest();
            
            var output = new Span<byte>(new byte[(65 - 1) / 2]);
            var cKey = new Span<byte>(_clientKey);

            var sw = new Stopwatch();
            sw.Start();

            for (var i = 0; i < numberOfIterations; i++)
            {
                open.GenerateSecret(cKey, output);
            }
            sw.Stop();

            var timePerOp = sw.ElapsedMilliseconds / (double)numberOfIterations;
            Console.WriteLine($"Time per operation {timePerOp}");
        }
    }
